void	freexit(t_ptr *ptr)
{
	int i;

	i = 0;
	if (*ptr == NULL)
		return (0);
	while ((ptr->tab)[i])
	{
		free((ptr->tab)[i]);
		i++;
	}
	free(ptr->tab);
	exit(0);
}
